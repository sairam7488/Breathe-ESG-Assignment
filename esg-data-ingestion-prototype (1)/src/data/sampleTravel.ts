export function generateTravelCSV(): string {
  const headers = ['report_id', 'entry_id', 'expense_type', 'transaction_date', 'vendor_name', 'description', 'origin_city', 'destination_city', 'origin_iata', 'destination_iata', 'cabin_class', 'distance_km', 'nights', 'amount', 'currency', 'employee_id', 'cost_center'];
  const rows = SAMPLE_TRAVEL_DATA.map(row =>
    headers.map(h => `"${(row as unknown as Record<string, string>)[h] || ''}"`)
      .join(',')
  );
  return [headers.join(','), ...rows].join('\n');
}
